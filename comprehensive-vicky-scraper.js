// Function to clean and format caption
function cleanCaption(caption) {
  if (!caption || caption.trim() === '') return null;
  
  // Remove URLs from captions
  let cleaned = caption.replace(/https?:\/\/[^\s]+/g, '').trim();
  
  // Remove hashtags at the beginning
  cleaned = cleaned.replace(/^#\w+\s*/, '').trim();
  
  // Remove excessive punctuation and clean up
  cleaned = cleaned.replace(/\s+/g, ' ').trim();
  
  if (cleaned.length > 250) {
    cleaned = cleaned.substring(0, 250) + '...';
  }
  
  return cleaned || null;
}

